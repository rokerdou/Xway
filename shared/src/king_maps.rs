#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_enmap_length() {
        assert_eq!(ENMAP.len(), 256);
    }

    #[test]
    fn test_demap_length() {
        assert_eq!(DEMAP.len(), 256);
    }

    #[test]
    fn test_enmap_unique() {
        // 验证ENMAP中每个值都是唯一的
        let mut seen = [false; 256];
        for &val in &ENMAP {
            assert!(!seen[val as usize], "ENMAP包含重复值: {}", val);
            seen[val as usize] = true;
        }
    }

    #[test]
    fn test_demap_unique() {
        // 验证DEMAP中每个值都是唯一的
        let mut seen = [false; 256];
        for &val in &DEMAP {
            assert!(!seen[val as usize], "DEMAP包含重复值: {}", val);
            seen[val as usize] = true;
        }
    }

    #[test]
    fn test_enmap_demap_inverse() {
        // 验证DEMAP是ENMAP的逆映射
        // 即: DEMAP[ENMAP[x]] == x
        for i in 0..256 {
            let encrypted = ENMAP[i];
            let decrypted = DEMAP[encrypted as usize];
            assert_eq!(decrypted as usize, i,
                "逆映射失败: ENMAP[{}] = {}, DEMAP[{}] = {}", i, encrypted, encrypted, decrypted);
        }
    }
}
